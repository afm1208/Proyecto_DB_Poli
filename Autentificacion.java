    //Boton para el inicio de sesion
    private void btnInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInicioActionPerformed

        try {
          
        String  tipou = (String) ComboBox1.getSelectedItem();
        Statement st;
        conex1 con = new conex1();
        String user = txtUsuario.getText();
        String password = String.valueOf(txtPsw.getPassword());
        String query= "SELECT * FROM aute WHERE usuario = '"+user+"'and pasw = '"+password+"'";
           st= (Statement) con.conectar().createStatement();
           ResultSet rs=st.executeQuery(query);
           //Condicional para loguearse
           if( tipou.equals("Usuario") && rs.next()){
               JOptionPane.showMessageDialog(this, "Autentificacion correcta");
               Conexion1 tabla = new Conexion1();
               tabla.setVisible(true);
               dispose();
           }else if ( tipou.equals("Organizacion") && rs.next()){
                JOptionPane.showMessageDialog(this, "Autentificacion correcta");
                Organizacion tablaOrg = new Organizacion();
                tablaOrg.setVisible(true);
               
           }else if( tipou.equals("Administrador") && rs.next()){
                JOptionPane.showMessageDialog(this, "Autentificacion correcta");
                Administrador tablaAdmin = new Administrador();
                tablaAdmin.setVisible(true);
        }else{
               JOptionPane.showMessageDialog(this, "Error usuario no encontrado");
           }
            
        } catch (SQLException e) {
            
             JOptionPane.showMessageDialog(null, "Error "+e.toString());
        }
        
    }//GEN-LAST:event_btnInicioActionPerformed

